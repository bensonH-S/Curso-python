'''     Exercício de programação com if e comparação      '''

primeiro_valor = input('Digite um valor: ')
segundo_valor = input('Digite outro valor: ')

if primeiro_valor > segundo_valor:
    print(f'O primeiro_valor = {primeiro_valor} é maior do que o segundo valor = {segundo_valor}')
elif segundo_valor > primeiro_valor:
    print(f'O segundo_valor = {segundo_valor} é maior do que o primeiro = {primeiro_valor}')